
<template>
  <main>
    <block>
      <h1>
        Set a new password! <omoji emoji="😃" /> <omoji emoji="☀️" />
      </h1>
      <form @submit.prevent="resetPassword()">
        <div class="input-wrap">
          <label for='password'> Password:</label>
          <input
            type="password"
            placeholder="password"
            v-model="password"
            id='password'
          />
        </div>
        <input-button>change password <loading-icon v-if="loading"/></input-button>
      </form>
    </block>
  </main>
</template>

<script setup lang="ts">
  definePageMeta({
    pagename: 'Password'
  })
  useHead({
    title: 'Password'
  })

  const supabase = useSupabaseClient()
  const auth = useSupabaseUser()
  const loading = ref(false)
  const password = ref('')
  const resetPassword = async () => {
    loading.value=true
    const { data, error } = await supabase.auth.updateUser({
      password: password.value
    })
    await ok.sleep(200);
    if(error){
      loading.value=false
      ok.log('error', 'password not changed', error)
    } else{
      loading.value=false
      navigateTo('/auth')
      ok.log('success', 'changed password')
    }
  }
</script>
<style scoped lang="scss">
  button{margin-top: sizer(2);}
</style>